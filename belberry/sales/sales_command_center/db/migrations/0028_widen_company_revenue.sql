-- numeric(14,2) переполнялся (max ~10^12). Расширяем до numeric(20,2) (до 10^18),
-- чтобы любые реальные/введённые значения выручки писались без overflow.
ALTER TABLE meetings ALTER COLUMN company_revenue TYPE numeric(20, 2);
