"""Единый порядок и названия стадий воронки Продажи [10].

Сверено с живой воронкой Bitrix 2026-06-18. Открытые/выигрышные стадии идут по
порядку пути; ОТВАЛ (C10:LOSE) и ОТЛОЖЕНО (C10:1) — терминальные, вне порядка
(order 0), помечаются отдельным флагом is_lost. Используется для furthest-stage
когорты (sync_funnel_cohort) и для справочника стадий в дашборде.
"""

# Стадия → порядковый номер пути (1..9).
STAGE_ORDER_10: dict[str, int] = {
    "C10:NEW": 1,               # Квалификация
    "C10:PREPAYMENT_INVOIC": 2,  # Подготовка БРИФа
    "C10:EXECUTING": 3,          # Подготовка КП
    "C10:UC_4SJOE4": 4,          # Защита КП (новая)
    "C10:FINAL_INVOICE": 5,      # Получить решение (бывш. «Догрев и переговоры»)
    "C10:UC_RJK0KE": 6,          # Получить реквизиты (новая)
    "C10:UC_KC7195": 7,          # Согласование договора (бывш. «Подготовка договора»)
    "C10:UC_755Z64": 8,          # Ожидаем оплату (новая)
    "C10:WON": 9,                # УСПЕХ / Оплата
}

STAGE_LABEL_10: dict[str, str] = {
    "C10:NEW": "Квалификация",
    "C10:PREPAYMENT_INVOIC": "Подготовка БРИФа",
    "C10:EXECUTING": "Подготовка КП",
    "C10:UC_4SJOE4": "Защита КП",
    "C10:FINAL_INVOICE": "Получить решение",
    "C10:UC_RJK0KE": "Получить реквизиты",
    "C10:UC_KC7195": "Согласование договора",
    "C10:UC_755Z64": "Ожидаем оплату",
    "C10:WON": "Оплата",
}

WON_STAGE_10 = "C10:WON"
LOST_STAGES_10 = {"C10:LOSE", "C10:1"}  # ОТВАЛ, ОТЛОЖЕНО


def stage_order(stage: str | None) -> int:
    """Порядок стадии пути; 0 — вне пути (терминальные/неизвестные)."""
    return STAGE_ORDER_10.get(stage or "", 0)


def furthest_order(stages) -> int:
    """Максимальный порядок среди достигнутых стадий (0 — путь не начат/неизвестен)."""
    return max((stage_order(s) for s in stages), default=0)
