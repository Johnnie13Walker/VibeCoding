/**
 * Локальный режим предпросмотра: открывает отчёты/дашборд без логина Bitrix —
 * чтобы разработчик (и ревью-агент) видел сервис, не проходя код от Ларисы.
 *
 * Двойная защита: включается ТОЛЬКО при SCC_PREVIEW=1 И вне продакшена.
 * В проде (NODE_ENV=production) флаг игнорируется всегда — даже если случайно
 * попадёт в окружение, отчёты наружу не откроются.
 */
export function isPreviewMode(): boolean {
  return process.env.SCC_PREVIEW === '1' && process.env.NODE_ENV !== 'production';
}
